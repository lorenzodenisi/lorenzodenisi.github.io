// Copy email to clipboard functionality
document.querySelectorAll('.contact-card[href^="mailto"]').forEach(card => {
    card.addEventListener('click', function(e) {
        if (e.ctrlKey || e.metaKey) {
            e.preventDefault();
            const email = this.querySelector('p').textContent;
            navigator.clipboard.writeText(email).then(() => {
                const originalText = this.querySelector('h3').textContent;
                this.querySelector('h3').textContent = 'Copied!';
                setTimeout(() => {
                    this.querySelector('h3').textContent = originalText;
                }, 2000);
            });
        }
    });
});

// Progress bar on scroll
function createProgressBar() {
    const progressBar = document.createElement('div');
    progressBar.style.cssText = `
        position: fixed;
        top: 0;
        left: 0;
        height: 3px;
        background: linear-gradient(90deg, #667eea 0%, #764ba2 100%);
        z-index: 9999;
        transition: width 0.1s ease;
    `;
    document.body.appendChild(progressBar);

    window.addEventListener('scroll', () => {
        const windowHeight = document.documentElement.scrollHeight - document.documentElement.clientHeight;
        const scrolled = (window.pageYOffset / windowHeight) * 100;
        progressBar.style.width = scrolled + '%';
    });
}

document.addEventListener('DOMContentLoaded', createProgressBar);

